package JAVA.Practice.ASS1;

public class Prob_7 {
    public static void main (String[] args){
        int x = 5, y = 6, z;
        System.out.println("Before swapping x = " + x + " y = " + y);
        z = x;
        x = y;
        y = z;
        System.out.println("After swapping x = " + x + " y = " + y);
    }
}
